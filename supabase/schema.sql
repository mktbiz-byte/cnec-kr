-- 5. updated_at 자동 갱신 트리거
CREATE OR REPLACE FUNCTION public.handle_updated_at()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = NOW();
    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DROP TRIGGER IF EXISTS on_corporate_accounts_update ON public.corporate_accounts;
CREATE TRIGGER on_corporate_accounts_update
    BEFORE UPDATE ON public.corporate_accounts
    FOR EACH ROW
    EXECUTE FUNCTION public.handle_updated_at();


